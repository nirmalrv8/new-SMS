    public void chkUser(){
        //check username
        
    
        Connection c = DBconnect.connect();        
        PreparedStatement ps;
        ResultSet result;
        reg = jTextField1.getText();
        
        
        
        if(reg.equals("")){
            JOptionPane.showMessageDialog(null, "please enter a reg num");
            return;
        }     
        
        String stt="";
        if(reg.startsWith("t")){
            type = "teacher";
            stt = "select * from teacher where Teacher_ID=?";
        }
        else{
            type = "student";
            stt = "select * from student where regNum=?";            
        }        

        try{
            ps = c.prepareStatement(stt); 
            
            ps.setString(1, reg);
            ResultSet re = ps.executeQuery();

            while(re.next()){
                jTextField2.setEnabled(true);
                jTextField1.setEnabled(false); 
                jTextField2.requestFocus();
                return;
            }    
            JOptionPane.showMessageDialog(null, "invalid reg num");
        }
        catch(SQLException ex){
            System.out.println("sql error " + ex.getMessage());
        }
    }
    
