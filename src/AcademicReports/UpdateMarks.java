    private void jButton1ActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jButton1ActionPerformed
        String sid=jTextField1.getText();
        String subid=jTextField2.getText();
        String mrk=jTextField3.getText();
        ResultSet r=null;
        Connection c=DBclass.connect();
        PreparedStatement ps;
        double marks;
        
                if("".equals(sid) || "".equals(subid) || "".equals(mrk))
                JOptionPane.showMessageDialog(null, "Please enter  the all values");
                
                else{         
                      
                   try{
                        ps = c.prepareStatement("select * from marks where regNum = ? and subjectID = ?");
                        ps.setString(1,sid);
                        ps.setString(2, subid);
                           
                        r=ps.executeQuery();
                        if(!r.isBeforeFirst())
                        JOptionPane.showMessageDialog(null,"Invalid student ID or subject ID. Please enter again");
                        else {
                           
                           try{
                               marks=Double.parseDouble(mrk);
                              
                               if(100 < marks || marks < 0)
                               JOptionPane.showMessageDialog(null, "Please enter a valid mark");
                             
                             else{
                                    Marks update=new Marks();
                                    update.updateMarks(sid, subid, marks);
                                    jTextField1.setText(null);
                                    jTextField2.setText(null);
                                    jTextField3.setText(null);
                                    
                                    Marks m=new Marks();
                                    ResultSet rs = m.getAll();
        
                                    jTable1.setModel(DbUtils.resultSetToTableModel(rs));
                             }
                           }
                           catch(NumberFormatException e){
                                 JOptionPane.showMessageDialog(null, "Please enter numbers only for mark text field");
                               }
                           
                           }
                       }
                       catch(SQLException e){
                        JOptionPane.showMessageDialog(null, e);
                       }
        
        } 

       
    }//GEN-LAST:event_jButton1ActionPerformed

