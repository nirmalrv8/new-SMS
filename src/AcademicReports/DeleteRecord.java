    private void jButton1ActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jButton1ActionPerformed
       

        
        
        sid=jTextField1.getText();
         subid=jTextField2.getText();
        PreparedStatement ps;
        Connection c = DBclass.connect();
        ResultSet r = null;
        
                if("".equals(sid) || "".equals(subid))
                JOptionPane.showMessageDialog(null, "Please enter  the all values");
                
                else{
                    
                        try{
                        ps = c.prepareStatement("select * from marks where regNum = ? and subjectID = ?");
                        ps.setString(1,sid);
                        ps.setString(2, subid);
                           
                        r=ps.executeQuery();
                        
                        if(!r.isBeforeFirst())
                        JOptionPane.showMessageDialog(null,"Invalid student ID or subject ID. Please enter again");
                            
                        else {                    
                               Marks remove = new Marks();
                               remove.removeMarks(subid, sid);
                               
                               jTextField1.setText(null);
                               jTextField2.setText(null);
                           
                           }
                       }
                       catch(SQLException e){
                        JOptionPane.showMessageDialog(null, e);
                       }
                        
                         Marks m=new Marks();
                         ResultSet rs = m.getAll();
        
                       jTable1.setModel(DbUtils.resultSetToTableModel(rs));
                
                }
        
    }//GEN-LAST:event_jButton1ActionPerformed

